public class Task_7 {
    public static void main(String[] args) {

        int n = 10;
        int c = 1;

        for (int i=0; i<n; i++) {
            for (int g=0; g<c; g++) {
                System.out.print("* ");
            }
            c++;
            System.out.println();
        }
    }
}